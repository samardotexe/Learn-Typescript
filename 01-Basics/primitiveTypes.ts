//These are the fundamental data types in TypeScript to define variables.


//1.String 
//Represents text.
let userName: string = "Samar";
let greeting: string = `Hello, ${userName}!`; // Template string


//2.Number
//Represents all numeric values (integers, floats, etc.).
let value: number = 10;
let temperature: number = -3.5;


//3.boolean
//Represents a true/false value.
let isLoggedIn: boolean = true;
let hasPermission: boolean = false;


//4.Null and Undefiend
let emptyValue: null = null;
let notAssigned: undefined = undefined;

